function normalizeLanguage(language) {
  return SUPPORTED_LANGUAGES.includes(language) ? language : "en";
}

function getStoredLanguage() {
  return normalizeLanguage(localStorage.getItem(I18N_STORAGE_KEY) || "en");
}

function setStoredLanguage(language) {
  localStorage.setItem(I18N_STORAGE_KEY, normalizeLanguage(language));
}

function getTranslation(language, key) {
  return translations[language]?.[key];
}

function captureDefaults() {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    if (!element.dataset.i18nDefault) {
      if (element.tagName === "TITLE") {
        element.dataset.i18nDefault = document.title;
      } else {
        element.dataset.i18nDefault = element.textContent;
      }
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    if (!element.dataset.i18nPlaceholderDefault) {
      element.dataset.i18nPlaceholderDefault = element.getAttribute("placeholder") || "";
    }
  });

  document.querySelectorAll("[data-i18n-value]").forEach((element) => {
    if (!element.dataset.i18nValueDefault) {
      element.dataset.i18nValueDefault = element.value;
    }
  });
}

function applyTranslations(language) {
  const currentLanguage = normalizeLanguage(language);
  document.documentElement.lang = currentLanguage;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const translated = getTranslation(currentLanguage, key);
    const fallback = element.dataset.i18nDefault || element.textContent;
    const value = translated || fallback;

    if (element.tagName === "TITLE") {
      document.title = value;
      element.textContent = value;
      return;
    }

    element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const translated = getTranslation(currentLanguage, key);
    const fallback = element.dataset.i18nPlaceholderDefault || "";
    element.setAttribute("placeholder", translated || fallback);
  });

  document.querySelectorAll("[data-i18n-value]").forEach((element) => {
    const key = element.dataset.i18nValue;
    const translated = getTranslation(currentLanguage, key);
    const fallback = element.dataset.i18nValueDefault || element.value;
    element.value = translated || fallback;
  });

  document.querySelectorAll(".lang-select").forEach((select) => {
    select.value = currentLanguage;
  });

  document.dispatchEvent(
    new CustomEvent("language-applied", {
      detail: { language: currentLanguage }
    })
  );
}

function setupLanguageSelectors() {
  document.querySelectorAll(".lang-select").forEach((select) => {
    select.addEventListener("change", (event) => {
      const language = normalizeLanguage(event.target.value);
      setStoredLanguage(language);
      applyTranslations(language);
    });
  });
}

document.addEventListener("DOMContentLoaded", () => {
  captureDefaults();
  setupLanguageSelectors();
  applyTranslations(getStoredLanguage());
});
